    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            String firstName = jTextField2.getText().trim();
            String lastName = jTextField3.getText().trim();
            String name = firstName + " " + lastName;
            LocalDate dob = jDateChooser1.getDate() != null ? jDateChooser1.getDate().toInstant().atZone(java.time.ZoneId.systemDefault()).toLocalDate() : null;
            String genderStr = (String) jComboBox3.getSelectedItem();
            String contact = jTextField1.getText().trim();
            String address = jTextArea1.getText().trim();

            if (firstName.isEmpty() || lastName.isEmpty() || dob == null || genderStr == null || contact.isEmpty() || address.isEmpty()) {
                JOptionPane.showMessageDialog(this, "All fields are required", "Input Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            Patient patient = new Patient();
            patient.setPatientId(pId);
            patient.setName(name);
            patient.setDob(dob);
            patient.setGender(Patient.Gender.valueOf(genderStr));
            patient.setContactInfo(contact);
            patient.setAddress(address);

            patientService.savePatient(patient, staff, "192.168.1.1");
            JOptionPane.showMessageDialog(this, "Patient Edit successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
            dispose();
            
        } catch (SecurityException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Access Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Failed to add patient: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

