    private void populateGenderComboBox() {
        jComboBox3.removeAllItems();
        for (Patient.Gender gender : Patient.Gender.values()) {
            jComboBox3.addItem(gender.name());
        }
    }

    private void loadPatientDetails() {
        Patient patient = patientService.getPatient();

        String fullname = patient.getName();

        String[] parts = fullname.trim().split("\\s+", 2);

        String firstName = parts[0];
        String lastName = (parts.length > 1) ? parts[1] : "";

        jTextField2.setText(firstName);
        jTextField3.setText(lastName);
        jTextArea1.setText(patient.getAddress());
        jComboBox3.setSelectedItem(String.valueOf(patient.getGender()));
        jTextField1.setText(patient.getContactInfo());

        LocalDate dob = patient.getDob();
        Date date = Date.from(dob.atStartOfDay(ZoneId.systemDefault()).toInstant());

        jDateChooser1.setDate(date);

    }

